package Control_satement;

public class alphabate_it_is_a_vowel_or_a_consonant {

	public static void main(String[] args) {

		char ch = 'i';

		if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
			System.out.println("vowel");
		} else {
			System.out.println("consonant");
		}

	}

}
