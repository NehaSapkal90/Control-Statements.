package Control_satement;

public class number_is_negative_positive_or_zero {

	public static void main(String[] args) {

		int num = 9;

		if (num > 0) {
			System.out.println("Positive number");
		} else if (num < 0) {
			System.out.println("Negative number");
		} else {
			System.out.println("zero");
		}

	}

}
